const char* i18n_t(const char *es, const char *en, const char *ru)
{
    switch (g_current_lang) {
        case LANG_EN: return en ? en : es;
        case LANG_RU: return ru ? ru : es;
        case LANG_PT:
        case LANG_FR:
        case LANG_DE:
            if (es) {
                for (int i = 0; g_trans[i].es != NULL; i++) {
                    if (strcmp(g_trans[i].es, es) == 0) {
                        const char *tr = (g_current_lang == LANG_PT) ? g_trans[i].pt :
                                         (g_current_lang == LANG_FR) ? g_trans[i].fr :
                                                                        g_trans[i].de;
                        return (tr && tr[0]) ? tr : (en ? en : es);
                    }
                }
                /* Not found in table — fall back to English */
                return en ? en : es;
            }
            return es;
        default: return es;
    }
}
